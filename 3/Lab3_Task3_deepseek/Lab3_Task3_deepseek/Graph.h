#pragma once

#include <vector>
#include <string>
#include <cstddef>

/**
 * Класс, представляющий ориентированный граф с весами дуг.
 * Хранит матрицу смежности и предоставляет методы для загрузки из файла
 * и выполнения поиска в ширину (BFS) от заданной вершины.
 */
class Graph
{
public:
    /**
     * Конструктор по умолчанию.
     * Инициализирует пустой граф.
     */
    Graph() = default;

    /**
     * Загружает граф из текстового файла.
     * Формат: первая строка – количество вершин n,
     * затем n строк по n целых чисел (матрица смежности).
     * @param fileName Имя файла.
     * @return true, если загрузка прошла успешно, иначе false.
     */
    bool loadFromFile(const std::string& fileName);

    /**
     * Выполняет поиск в ширину от вершины startVertex (1‑индексация).
     * Возвращает вектор номеров вершин в порядке обхода (1‑индексация).
     * Если на шаге есть несколько равноправных вершин, они перечисляются
     * в порядке возрастания номеров.
     * @param startVertex Номер начальной вершины (1..n).
     * @return Вектор достижимых вершин в порядке BFS.
     */
    std::vector<int> bfs(int startVertex) const;

    /**
     * Возвращает количество вершин в графе.
     */
    std::size_t getVertexCount() const { return adjacencyMatrix.size(); }

    /**
     * Проверяет, существует ли дуга из вершины u в вершину v (1‑индексация).
     */
    bool hasEdge(int u, int v) const;

private:
    std::vector<std::vector<int>> adjacencyMatrix; // Матрица смежности (0-база)

    /**
     * Преобразует номер вершины из 1-индексации в 0-базу.
     * Выбрасывает исключение при выходе за допустимые пределы.
     */
    std::size_t toIndex(int vertex) const;
};